    public void viewPatientDetails(String patientUsername){
        Patient patient = new Patient();
        int patientFound = 0;
        for(Patient p:PatientDirectory.getPatientDir()){
            if(patientUsername.equals(p.getEmailId())){
                patient = p;
                patientFound = 1;
            }
        }
        
        patientDetailsName.setText(patient.getName());
//        txtAge.setText(String.valueOf( patient.getAge()));
        patientDetailsContact.setText(String.valueOf(patient.getContactNo()));
        patientDetailsPatientID.setText(String.valueOf(patient.getPatientId()));
        patientGender.setSelectedItem((String)patient.getGender());
        
        patientDetailsHouse.setText(String.valueOf(patient.residence.getHouseNo()));
        patientDetailsCommunity.setText(patient.residence.getCommunityName());
        
        patientDetailsCity.setText(patient.residence.getNewCity());
        patientDetailsState.setText(patient.residence.getState());
        patientDetailsZipCode.setText(String.valueOf(patient.residence.getZip()));
        
        BloodGrpCombo.setSelectedItem(patient.getBloodGroup());
        patientDetailsHeight.setText(String.valueOf(patient.getPatientHeight()));
        patientDetailsWeight.setText(String.valueOf(patient.getPatientWeight()));   
        JOptionPane.showMessageDialog(this, "Your Information is Updated");
        
    }
 

