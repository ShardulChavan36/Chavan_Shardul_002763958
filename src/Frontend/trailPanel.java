    private TableRowSorter searcher;
    private void dispTable(){
        DefaultTableModel model = (DefaultTableModel) empDisplayTable.getModel();
        model.setRowCount(0);
        searcher = new TableRowSorter<>(model);
        empDisplayTable.setRowSorter(searcher);
        for (EmpDetails e:saved_data.getSaved_data()){
            Object[] row=new Object[11];
        row[0]=e;
        row[1]=e.getAge();
        row[2]=e.getGender();
        row[3]=e.getEmpid();
        row[4]=e.getStart_date();
        row[5]=e.getLevel();
        row[6]=e.getTeam_info();
        row[7]=e.getPos_title();
        row[8]=e.getCell_no();
        row[9]=e.getEmail_id();
        row[10]=e.getIcon();
        model.addRow(row);
        }
        
        searchField.getDocument().addDocumentListener( new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                search(searchField.getText());
            }
            @Override
            public void removeUpdate(DocumentEvent e) {
                search(searchField.getText());
            }
            @Override
            public void changedUpdate(DocumentEvent e) {
                search(searchField.getText());
            }
            public void search(String str) {
                if (str.length() == 0) {
                    searcher.setRowFilter(null);
                }
                else {
                    searcher.setRowFilter(RowFilter.regexFilter(str));
                }
            }

        });
      
    }
    
    
    

